package pubsub

import (
	"encoding/json"
	"fmt"
	"log"

	amqp "github.com/rabbitmq/amqp091-go"
)

type AckType int

const (
	Ack AckType = iota
	NackRequeue
	NackDiscard
)

func SubscribeJSON[T any](
	conn *amqp.Connection,
	queueName string,
	queueType SimpleQueueType,
	exchange,
	key string,
	handler func(T) AckType,
) error {
	channel, _, err := DeclareAndBindQueue(conn, queueName, queueType, exchange, key)
	if err != nil {
		return fmt.Errorf("failed to declare and bind queue: %w", err)
	}

	const (
		autoGeneratedConsumerName = ""
		disableAutoAck            = false
		nonExclusive              = false
		local                     = false
		wait                      = false
	)
	deliver, err := channel.Consume(
		queueName,
		autoGeneratedConsumerName,
		disableAutoAck,
		nonExclusive,
		local,
		wait,
		nil,
	)
	if err != nil {
		return fmt.Errorf("failed to start delivered messages consumption: %w", err)
	}

	go func() {
		for msg := range deliver {
			var val T
			if err := json.Unmarshal(msg.Body, &val); err != nil {
				log.Printf("failed to unmarshal message body: %v", err)
			}

			const (
				single  = false
				requeue = true
				discard = false
			)
			switch handler(val) {
			case Ack:
				if err := msg.Ack(single); err != nil {
					log.Printf("failed to ack msg: %v", err)
				}
			case NackRequeue:
				if err := msg.Nack(single, requeue); err != nil {
					log.Printf("failed to nack msg with requeue: %v", err)
				}
			case NackDiscard:
				if err := msg.Nack(single, discard); err != nil {
					log.Printf("failed to nack msg with discard: %v", err)
				}

			}
		}
	}()

	return nil
}
